    n = 0
    for i in start_button:
        n += 1
        if i == start_button[-2]:
            button.add(KeyboardButton(text=i))

        elif i == start_button[-1]:
            button.add(KeyboardButton(text=i))

        elif n == 1:
            one = KeyboardButton(text=i)

        elif n == 2:
            button.row(
                one, KeyboardButton(text=i)
            )
            n = 0
    return button



